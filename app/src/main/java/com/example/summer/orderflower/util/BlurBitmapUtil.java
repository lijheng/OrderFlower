package com.example.summer.orderflower.util;

import android.content.Context;
import android.graphics.Bitmap;
import android.renderscript.Allocation;
import android.renderscript.Element;
import android.renderscript.RenderScript;
import android.renderscript.ScriptIntrinsicBlur;

/**
 * Created by summer on 2017/8/14.
 */

public class BlurBitmapUtil {

    //图片缩放比例
    private static final float BITMAP_SCALE = 0.4f;

    /**
     * 模糊图片的具体方法
     *
     * @param context 上下文对象
     * @param image   需要模糊的图片
     * @return 模糊处理后的图片
     */
    public static Bitmap blurBitmap(Context context, Bitmap image, float blurRadius) {
        // 计算图片缩小后的长宽
        int width = Math.round(image.getWidth() * BITMAP_SCALE);
        int height = Math.round(image.getHeight() * BITMAP_SCALE);

        // 将缩小后的图片做为预渲染的图片
        Bitmap inputBitmap = Bitmap.createScaledBitmap(image, width, height, false);
        // 创建一张渲染后的输出图片
        Bitmap outputBitmap = Bitmap.createBitmap(inputBitmap);

        // 创建RenderScript内核对象
        RenderScript rs = RenderScript.create(context);
        // 创建一个模糊效果的RenderScript的工具对象
        ScriptIntrinsicBlur blurScript = ScriptIntrinsicBlur.create(rs, Element.U8_4(rs));

        // 由于RenderScript并没有使用VM来分配内存,所以需要使用Allocation类来创建和分配内存空间
        // 创建Allocation对象的时候其实内存是空的,需要使用copyTo()将数据填充进去
        Allocation tmpIn = Allocation.createFromBitmap(rs, inputBitmap);
        Allocation tmpOut = Allocation.createFromBitmap(rs, outputBitmap);

        // 设置渲染的模糊程度, 25f是最大模糊度
        blurScript.setRadius(blurRadius);
        // 设置blurScript对象的输入内存
        blurScript.setInput(tmpIn);
        // 将输出数据保存到输出内存中
        blurScript.forEach(tmpOut);

        // 将数据填充到Allocation中
        tmpOut.copyTo(outputBitmap);

        return outputBitmap;
    }

    /**
     * 图片的透明处理方法
     * @param bitmap 需要模糊的图片
     * @param number 透明度，取值[0-100],0表示完全透明
     * @return 模糊后的图片
     */
    public static Bitmap getTransparentBitmap(Bitmap bitmap,int number){
        int[] argb = new int[bitmap.getWidth()*bitmap.getHeight()];
//        获得图片的ARGB值
        bitmap.getPixels(argb,0,bitmap.getWidth(),0,0,bitmap.getWidth(),bitmap.getHeight());
        number = number*255/100;
        for (int i=0;i<argb.length;i++){
            argb[i] = (number<<24)|(argb[i]&0x00ffffff);
        }
        bitmap = Bitmap.createBitmap(argb,bitmap.getWidth(),bitmap.getHeight(), Bitmap.Config.ARGB_8888);
        return bitmap;
    }
}
